public class IceCream {

    //Attributes
    public String flavor;
    public String cone;
    public boolean withTopping;

    //Methods - Getters and Setters for each one of the attributes

    //flavor
    public String getFlavor() {
        return flavor;
    }
    public void setFlavor(String flavor) {
        this.flavor = flavor;
    }

    //cone
    public String getCone() {
        return cone;
    }
    public void setCone(String cone) {
        this.cone = cone;
    }

    //withTopping
    public boolean isWithTopping() {
        return withTopping;
    }
    public void setWithTopping(boolean withTopping) {
        this.withTopping = withTopping;
    }
}
